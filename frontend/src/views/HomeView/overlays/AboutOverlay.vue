    <li>using semantic concepts in describing digitized cultural objects,</li>
    <li>annotating different types of media, and</li>
    <li>introducing automation in assisted data curation and annotation workflows.</li>
  </ol>
  <p>
    The service framework is accessible both via a frontend web portal and as a separate data service, with a special focus on integration
